@router.post("/forecast", response_model=ForecastResponse, summary="Forecast rating impact for every score")
def api_forecast(req: ForecastRequest):
    rows = forecast_svc.forecast_matrix(
        req.r1, req.r2, req.r3, req.r4,
        target=req.target,
        rel1=req.rel1, rel2=req.rel2, rel3=req.rel3, rel4=req.rel4,
    )
    return ForecastResponse(
        expected_games_team1=rows[0].expected_games_team1 if rows else 0.0,
        rows=[
            ForecastRowDTO(
                games1=row.games1,
                games2=row.games2,
                winner=row.winner,
                expected_games_team1=row.expected_games_team1,
                impacts=[
                    PlayerImpactDTO(
                        player=imp.player,
                        pre_rating=imp.pre_rating,
                        delta=imp.delta,
                        post_rating=imp.post_rating,
                    )
                    for imp in row.impacts
                ],
            )
            for row in rows
        ],
    )


