def _voter_key(request: Request) -> str:
    # Vercel puts the client IP in x-forwarded-for; fall back to peer addr.
    xff = request.headers.get("x-forwarded-for", "")
    if xff:
        return xff.split(",")[0].strip()
    return request.client.host if request.client else "anon"


@router.get("/fupr/{player_id}", response_model=FuprAggregateDTO)
def api_fupr_aggregate(player_id: int, session: Session = Depends(get_session)):
    agg = fupr_svc.aggregate(session, player_id)
    return FuprAggregateDTO(
        player_id=agg.player_id,
        count=agg.count,
        weighted_mean=agg.weighted_mean,
        median=agg.median,
        histogram=agg.histogram,
    )


@router.post("/fupr/{player_id}/votes", status_code=status.HTTP_201_CREATED)
def api_fupr_vote(
    player_id: int,
    req: FuprVoteRequest,
    request: Request,
    session: Session = Depends(get_session),
):
    # Confirm player exists
    p = session.get(JuprPlayer, player_id)
    if p is None:
        raise HTTPException(status_code=404, detail="player not found")
    try:
        fupr_svc.cast_vote(
            session,
            player_id=player_id,
            rating=req.rating,
            confidence=req.confidence,
            voter_key=_voter_key(request),
            comment=req.comment,
        )
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    agg = fupr_svc.aggregate(session, player_id)
    return FuprAggregateDTO(
        player_id=agg.player_id,
        count=agg.count,
        weighted_mean=agg.weighted_mean,
        median=agg.median,
        histogram=agg.histogram,
    )


